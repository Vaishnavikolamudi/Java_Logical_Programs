import java.util.Scanner;
class ReverseOrder
{
     public static void main(String[] args)
     {
      Scanner sc=new Scanner(System.in);
      int i, n;
      System.out.println("Enter n value");
      n=sc.nextInt();
      i=n;
      while(i>=1)
      {
       System.out.println(i);
       i--;
       }
}
}
